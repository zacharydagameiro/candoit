import http from "node:http"
import { spawn } from "node:child_process"

const PORT = Number(process.env.PI_SEARCH_SERVER_PORT ?? 8787)
const HOST = process.env.PI_SEARCH_SERVER_HOST ?? "127.0.0.1"
const TIMEOUT_MS = Number(process.env.PI_SEARCH_TIMEOUT_MS ?? 120000)
const MOCK_ON_FAILURE = process.env.PI_SEARCH_MOCK_ON_FAILURE !== "false"
const DEBUG = process.env.PI_SEARCH_DEBUG !== "false"
const PI_MODEL = process.env.PI_MODEL ?? "openrouter/moonshotai/kimi-k2.5"

function readJson(req) {
  return new Promise((resolve, reject) => {
    let body = ""

    req.on("data", (chunk) => {
      body += chunk
    })

    req.on("end", () => {
      if (!body) {
        resolve({})
        return
      }

      try {
        resolve(JSON.parse(body))
      } catch {
        reject(new Error("Request body must be valid JSON"))
      }
    })

    req.on("error", reject)
  })
}

function writeJson(res, status, body) {
  res.writeHead(status, { "Content-Type": "application/json" })
  res.end(JSON.stringify(body))
}

function extractJsonArray(rawOutput) {
  const trimmed = rawOutput.trim()
  const startIndex = trimmed.indexOf("[")
  const endIndex = trimmed.lastIndexOf("]")

  if (startIndex === -1 || endIndex === -1 || endIndex < startIndex) {
    throw new Error("Supplier search did not return a JSON array")
  }

  const candidate = trimmed.slice(startIndex, endIndex + 1)
  JSON.parse(candidate)
  return candidate
}

function buildPrompt(payload) {
  const title = typeof payload.title === "string" ? payload.title.trim() : ""
  const description = typeof payload.description === "string"
    ? payload.description.trim()
    : ""
  const category = typeof payload.category === "string"
    ? payload.category.trim()
    : ""

  if (!title) {
    throw new Error("Missing requirement title")
  }

  return [
    "Find suppliers for this requirement.",
    `Requirement: ${title}`,
    category ? `Category: ${category}` : "",
    description ? `Description: ${description}` : "",
    "Search for wholesale or manufacturing suppliers.",
    "Return ONLY valid JSON.",
    "Do not include markdown fences or explanation text.",
    "Use exactly this schema and return a JSON array:",
    "[",
    "  {",
    '    "name": "Company Name",',
    '    "website": "https://example.com",',
    '    "email": "contact@example.com or null",',
    '    "phone": "+1-555-555-5555 or null",',
    '    "country": "USA or null",',
    '    "notes": "brief description"',
    "  }",
    "]",
  ].filter(Boolean).join("\n")
}

function buildMockSuppliers(payload) {
  const title = typeof payload.title === "string" && payload.title.trim()
    ? payload.title.trim()
    : "Requirement"
  const category = typeof payload.category === "string" && payload.category.trim()
    ? payload.category.trim()
    : "General"

  return JSON.stringify([
    {
      name: `${title} Source Co.`,
      website: "https://supplier-one.example.com",
      email: "sales@supplier-one.example.com",
      phone: "+1-555-0101",
      country: "USA",
      notes: `${category} supplier generated by local fallback.`,
    },
    {
      name: `${title} Manufacturing Group`,
      website: "https://supplier-two.example.com",
      email: "hello@supplier-two.example.com",
      phone: "+1-555-0102",
      country: "Canada",
      notes: `Wholesale ${title.toLowerCase()} supplier generated by local fallback.`,
    },
    {
      name: `${title} Wholesale Ltd.`,
      website: "https://supplier-three.example.com",
      email: "contact@supplier-three.example.com",
      phone: "+1-555-0103",
      country: "Mexico",
      notes: `Backup demo supplier for ${title}.`,
    },
  ])
}

function runPiSearch(payload) {
  return new Promise((resolve, reject) => {
    const prompt = buildPrompt(payload)
    if (DEBUG) {
      console.log("pi-search-server prompt:")
      console.log(prompt)
    }
    const child = spawn(
      "pi",
      [
        "-p",
        "--model",
        PI_MODEL,
        "--mode",
        "text",
        "--no-session",
        "--skill",
        "brave-search",
        prompt,
      ],
      {
        cwd: process.cwd(),
        env: {
          ...process.env,
        },
        stdio: ["ignore", "pipe", "pipe"],
      },
    )

    let stdout = ""
    let stderr = ""
    let settled = false

    if (DEBUG) {
      console.log(`pi-search-server spawned pid=${child.pid ?? "unknown"}`)
    }

    const timer = setTimeout(() => {
      if (settled) {
        return
      }

      settled = true
      child.kill("SIGTERM")
      if (DEBUG) {
        console.log("pi-search-server timeout")
        console.log(`pi-search-server partial stdout:\n${stdout.slice(0, 4000)}`)
        console.log(`pi-search-server partial stderr:\n${stderr.slice(0, 4000)}`)
      }
      reject(new Error(`pi process timed out after ${TIMEOUT_MS}ms`))
    }, TIMEOUT_MS)

    child.stdout.on("data", (chunk) => {
      const text = chunk.toString()
      stdout += text
      if (DEBUG) {
        console.log(`pi-search-server stdout chunk:\n${text.slice(0, 2000)}`)
      }
    })

    child.stderr.on("data", (chunk) => {
      const text = chunk.toString()
      stderr += text
      if (DEBUG) {
        console.log(`pi-search-server stderr chunk:\n${text.slice(0, 2000)}`)
      }
    })

    child.on("error", (error) => {
      if (DEBUG) {
        console.log(`pi-search-server spawn error: ${error.message}`)
      }
      reject(error)
    })

    child.on("close", (code) => {
      if (settled) {
        return
      }

      settled = true
      clearTimeout(timer)
      if (DEBUG) {
        console.log(`pi-search-server closed with code=${code ?? "null"}`)
        console.log(`pi-search-server final stdout:\n${stdout.slice(0, 4000)}`)
        console.log(`pi-search-server final stderr:\n${stderr.slice(0, 4000)}`)
      }

      if (code !== 0) {
        reject(
          new Error(
            stderr.trim() || `pi process failed with exit code ${code ?? "unknown"}`,
          ),
        )
        return
      }

      try {
        resolve(extractJsonArray(stdout))
      } catch (error) {
        const message = error instanceof Error ? error.message : "Unknown error"
        reject(
          new Error(
            `${message}. Raw output: ${stdout.trim().slice(0, 1200)}`,
          ),
        )
      }
    })
  })
}

const server = http.createServer(async (req, res) => {
  if (req.method === "GET" && req.url === "/health") {
    writeJson(res, 200, { ok: true })
    return
  }

  if (req.method !== "POST" || req.url !== "/find-suppliers") {
    writeJson(res, 404, { error: "Not found" })
    return
  }

  try {
    const payload = await readJson(req)
    let result

    try {
      result = await runPiSearch(payload)
    } catch (error) {
      if (!MOCK_ON_FAILURE) {
        throw error
      }

      const message = error instanceof Error ? error.message : "Unexpected error"
      console.warn(`pi-search-server fallback: ${message}`)
      result = buildMockSuppliers(payload)
    }

    res.writeHead(200, { "Content-Type": "application/json" })
    res.end(result)
  } catch (error) {
    writeJson(res, 500, {
      error: error instanceof Error ? error.message : "Unexpected error",
    })
  }
})

server.listen(PORT, HOST, () => {
  console.log(`pi-search-server listening on http://${HOST}:${PORT}`)
})
